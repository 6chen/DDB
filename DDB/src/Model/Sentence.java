package Model;

public class Sentence {
	private String sentenceNo;
	private String sentenceContent;
	private String sentenceInsertDate;
	private String bizRequirNo;
	public String getSentenceNo() {
		return sentenceNo;
	}
	public void setSentenceNo(String sentenceNo) {
		this.sentenceNo = sentenceNo;
	}
	public String getSentenceContent() {
		return sentenceContent;
	}
	public void setSentenceContent(String sentenceContent) {
		this.sentenceContent = sentenceContent;
	}
	public String getSentenceInsertDate() {
		return sentenceInsertDate;
	}
	public void setSentenceInsertDate(String sentenceInsertDate) {
		this.sentenceInsertDate = sentenceInsertDate;
	}
	public String getBizRequirNo() {
		return bizRequirNo;
	}
	public void setBizRequirNo(String bizRequirNo) {
		this.bizRequirNo = bizRequirNo;
	}
	
	
}
